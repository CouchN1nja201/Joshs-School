class ArithmeticTest {
	public static void main (String args[] ) {
		short x = 6;
		int y = 4;
		float a = 12.5f;
		float b = 7f;

		System.out.println ("x is " + x + ", y is " + y);
		System.out.println ("x + y = " + (x + y));
		System.out.println ("X - y = " + (x - y));
		System.out.println ("x / y = " + (x / y));
		System.out.println ("x % y = " + (x % y));
		System.out.println ("a is " + a + ", b is " + b);
		System.out.println ("a / b = " + (a / b));
	}

}
